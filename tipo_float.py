"""
Tipo Float
Tipo Real, decimal

Casas Decimais
print(f'{valor:.2f}') ## limita a 2 casas decimais na exibição
OBS: O separador de cadas decimais na programação é ponto e não virgula.
"""


# Errado
valor = 1,44
print(f'{valor}')
print(type(valor))

# Carto
valor = 1.44
print(f'{valor}')
print(type(valor))

# Dupla atribuição
valor1, valor2 = 1, 44
print(f'{valor1}')
print(type(valor1))
print(f'{valor2}')
print(type(valor2))

# Conversão float para inteiro
res = int(valor)
print(f'{valor}')
print(type(res))

# Podemos trabalhar com números complexos

variavel = 5j
print(type(variavel))


