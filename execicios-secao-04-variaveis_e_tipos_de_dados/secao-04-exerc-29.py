# 19 - metros para  metros cubicos

valor1 = float(input("Digite o nota do 1º Bimestre: "))
valor2 = float(input("Digite o nota do 2º Bimestre: "))
valor3 = float(input("Digite o nota do 3º Bimestre: "))
valor4 = float(input("Digite o nota do 4º Bimestre: "))
media = (valor1 + valor2 + valor3 + valor4) / 4
print(f'A Média Anual é: {media}')
