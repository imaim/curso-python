# 19 - metros para  metros cubicos

valor1 = float(input("Digite o valor 1: "))
valor2 = float(input("Digite o valor 2: "))
valor3 = float(input("Digite o valor 3: "))
soma = valor1 ** 2 + valor2 ** 2 + valor3 ** 2
print(f'A Soma dos Quadrados dos valores {valor1}, {valor2} e {valor3} é igual à: {soma}')
